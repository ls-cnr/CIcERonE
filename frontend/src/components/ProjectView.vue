        Go Back to Dashboard
      </button>
    </div>

    <div class="project-info">
      <label for="project-title">Title:</label>
      <input id="project-title" v-model="project.title" class="project-title" @input="enableUpdate" />

      <label for="project-description">Description:</label>
      <textarea id="project-description" v-model="project.description" class="project-description" @input="enableUpdate"></textarea>
    </div>

    <div class="update-btn-container">
      <button @click="updateProject" :disabled="!isUpdateEnabled" class="btn update-btn">Update</button>
      <span v-if="updateMessage" class="tooltip">{{ updateMessage }}</span>
    </div>

