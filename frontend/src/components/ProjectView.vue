<template>
  <div v-if="project" class="project-view">
    <header class="top-nav">
      <button @click="goToDashboard" class="btn icon-btn">
        <span class="material-icons">arrow_back</span>
        Back to Dashboard
      </button>
      <h1>{{ project.title }}</h1>
      <button @click="editProject" class="btn icon-btn">
        <span class="material-icons">edit</span>
        Edit
      </button>
    </header>

    <div class="project-content">
      <div class="mental-space-container">
        <div class="toolbar top-toolbar">
          <span class="toolbar-label">Mental Space Lattice</span>
          <div class="toolbar-buttons">
            <button class="btn" disabled>Upload Doc</button>
            <button @click="acquireViewpoint" class="btn icon-btn">
              <span class="material-icons">add_circle</span>
              Acquire Viewpoint
            </button>
          </div>
        </div>

        <div class="mental-space-content markdown-rendered" v-html="renderedMarkdown"></div>

        <div class="toolbar bottom-toolbar">
          <span class="toolbar-caption">generated by AI</span>
          <div class="toolbar-buttons">
            <button @click="refreshMentalSpace" class="btn icon-btn">
              <span class="material-icons">refresh</span>
              Refresh
            </button>
            <button @click="copyMentalSpace" class="btn icon-btn">
              <span class="material-icons">content_copy</span>
              Copy
            </button>
          </div>
        </div>

        <div v-if="toolbarMessage" class="toolbar-message">{{ toolbarMessage }}</div>
      </div>
    </div>
  </div>
  <div v-else class="loading">Loading project...</div>
</template>

<script>
import { ref, onMounted, computed, watch } from 'vue';
import { useRoute, useRouter } from 'vue-router';
import axios from 'axios';
import { marked } from 'marked';
import DOMPurify from 'dompurify';

export default {
  name: 'ProjectView',
  setup() {
    const route = useRoute();
    const router = useRouter();
    const project = ref(null);
    const toolbarMessage = ref('');

    const renderedMarkdown = computed(() => {
      if (project.value?.mental_space_lattice) {
        const rawHtml = marked(project.value.mental_space_lattice);
        return DOMPurify.sanitize(rawHtml);
      }
      return '';
    });

    const fetchProjectDetails = async () => {
      try {
        const projectId = route.params.id;
        const token = localStorage.getItem('token');
        const response = await axios.get(`${import.meta.env.VITE_API_URL}/projects/${projectId}`, {
          headers: {
            'Authorization': `Bearer ${token}`
          }
        });
        project.value = response.data;
      } catch (error) {
        console.error('Error fetching project details:', error);
      }
    };

    const goToDashboard = () => {
      router.push('/dashboard');
    };

    const editProject = () => {
      router.push(`/project/${project.value.id}/edit`);
    };

    const refreshMentalSpace = async () => {
      await fetchProjectDetails();
      showToolbarMessage('Mental space refreshed!');
    };

    const copyMentalSpace = () => {
      navigator.clipboard.writeText(project.value.mental_space_lattice)
        .then(() => showToolbarMessage('Copied to clipboard!'))
        .catch(() => showToolbarMessage('Failed to copy.'));
    };

    const showToolbarMessage = (message) => {
      toolbarMessage.value = message;
      setTimeout(() => {
        toolbarMessage.value = '';
      }, 3000);
    };

    const acquireViewpoint = () => {
      if (project.value?.id) {
        router.push({ name: 'AcquireViewpoint', params: { id: project.value.id } });
      } else {
        console.error('Project ID not available');
      }
    };

    onMounted(fetchProjectDetails);

    watch(() => route.params.id, fetchProjectDetails);

    return {
      project,
      toolbarMessage,
      renderedMarkdown,
      goToDashboard,
      editProject,
      refreshMentalSpace,
      copyMentalSpace,
      acquireViewpoint
    };
  }
};
</script>

