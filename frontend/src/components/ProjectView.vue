      </div>

     <div class="mental-space-content markdown-rendered" v-html="renderedMarkdown"></div>

      <div class="toolbar bottom-toolbar">
        <span class="toolbar-caption">generated by AI</span>
        <div class="toolbar-buttons">
          <button @click="refreshMentalSpace" class="btn icon-btn">
            <span class="material-icons">refresh</span>
